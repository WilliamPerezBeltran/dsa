#include <stdio.h>

int main(){
	// format specifiers
	// int -> %d
	// float -> %f
	// char -> %c

	int myNumber = 23;
	float myFloat = 23.23;
	char myChar = 'd';

	printf("this is a number %d\n", myNumber);
	printf(" this is a float %f\n", myFloat);
	printf("this is a char %c\n", myChar);
	return 0;
}
